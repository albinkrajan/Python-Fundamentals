#arithmetic operations on integer numbers
i1 = int(input("Enter the integer number:"))
i2 = int(input("Enter the 2nd integer number:"))
print(i1+i2)
print(i1-i2)
print(i1*i2)
print(i1/i2)
print(i1%i2)
print(i1**i2)
print(i1//i2)

#arithmetic operations on numbers with decimal points
f1 = float(input("Enter the 1st complex number"))
f2 = float(input("Enter the 1st complex number"))
print(f1+f2)
print(f1-f2)
print(f1*f2)
print(f1/f2)
print(f1%f2)
print(f1**f2)
print(f1//f2)

